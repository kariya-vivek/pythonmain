# i) Arithmetic
print(3 + 2, 3 - 2, 3 * 2, 3 / 2, 3 % 2, 3 ** 2, 3 // 2)

# ii) Relational
print(3 > 2, 3 < 2, 3 == 3, 3 != 2)

# iii) Assignment
x = 10
x += 5
print(x)

# iv) Logical
print(True and False, True or False, not True)

# v) Bitwise
print(2 & 3, 2 | 3, 2 ^ 3, ~2, 2 << 1, 2 >> 1)

# vi) Ternary
a = 10
b = 20
min_val = a if a < b else b
print(min_val)
